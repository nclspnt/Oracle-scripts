set lines 200 pages 200
col SRC_DBNAME format a15
col DISK_BYTES_TB format 999999999999999999999999999999999.99
col FLASH_BYTES_TB format 999999999999999999999999999999999.99
col PCT_FLASH_BYTES format 999.99
select SRC_DBNAME, 
sum(DISK_BYTES)/1024/1024/1024/1024 DISK_BYTES_TB, 
sum(FLASH_BYTES)/1024/1024/1024/1024 FLASH_BYTES_TB, 
round(100/(sum(FLASH_BYTES)+sum(DISK_BYTES))*sum(FLASH_BYTES),2) PCT_FLASH_BYTES 
from DBA_HIST_CELL_DB group by SRC_DBNAME order by 2 desc ;


